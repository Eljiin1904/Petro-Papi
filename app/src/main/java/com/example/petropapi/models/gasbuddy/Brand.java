public class Brand {
    private String brandId;
    private String brandingType;
    private String imageUrl;
    private String name;

    // Getters
    public String getBrandId() {
        return brandId;
    }

    public String getBrandingType() {
        return brandingType;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public String getName() {
        return name;
    }
}
